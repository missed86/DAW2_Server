def puntos(input, sep = ".", numSep = 999):
    output = ""
    pos = 0
    count = numSep
    for char in input:
        if count>0 and pos != 0 and pos%3 == 0:
            output+=sep
            count-=1
        output += char
        pos+=1
    return output
        
